from api.typhoon_api import ask_typhoon  

def classify_intent(question: str) -> str: 
    """
    จำแนกเจตนา (intent) ของข้อความที่ผู้ใช้ถาม
    โดยใช้ Typhoon API เพื่อวิเคราะห์ข้อความ
    """
    prompt = f"""  # สร้าง prompt ให้ Typhoon เพื่อให้จำแนก intent
คุณคือนักวิเคราะห์ระบบที่มีหน้าที่จำแนกเจตนา (intent) ของข้อความที่ผู้ใช้ถามเข้ามา
โดยต้องเลือกเพียงหนึ่ง intent จากรายการต่อไปนี้ พร้อมตัวอย่างข้อความ:

1. ranking: ถามถึง 'รายชื่อโปรเจคที่เกี่ยวข้องกับเรื่องต่างๆ' 
   - ตัวอย่าง: "ปีพ.ศ.2566มีหัวข้อโปรเจคอะไรบ้าง?" 
   - ตัวอย่าง: "อยากดูปริญญานิพนธ์เกี่ยวกับการเกษตร"
   - ตัวอย่าง: "โปรเจคที่เกี่ยวกับIOTมีอะไรบ้าง"

2. download: ผู้ใช้ต้องการ 'ลิงก์ดาวน์โหลด' ไฟล์ PDF
   - ตัวอย่าง: "อยากดาวน์โหลดปริญญานิพนธ์เรื่อง AI"
   - ตัวอย่าง: "ขอไฟล์แอปพลิเคชันสำหรับเฝ้าระวังและติดตามปริมาณคาร์บอนฟุตพริ้นท์จากกิจกรรมประจำวัน"

3. metadata_query: ถามเกี่ยวกับ metadata เช่น ปี, ชื่อผู้ทำ, อาจารย์
   - ตัวอย่าง: "ใครเป็นที่ปรึกษาระบบแนะนำการท่องเที่ยวตามพฤติกรรม"
   - ตัวอย่าง: "โปรเจคแอปพลิเคชันสำหรับเฝ้าระวังและติดตามปริมาณคาร์บอนฟุตพริ้นท์จากกิจกรรมประจำวันใครเป็นคนทำ"

4. answer_based_on_documents: คำถามลึก ๆ ต้องใช้เนื้อหาในเอกสาร
   - ตัวอย่าง: "สรุปบทที่ 3 ของระบบจัดการร้านอาหารบ้านสวนให้หน่อย"
   - ตัวอย่าง: "อธิบายวิธีการทำระบบจัดการร้านอาหารบ้านสวนหน่อย"

5. lecturer_info: ถามเกี่ยวกับอาจารย์ เช่น เชี่ยวชาญด้านไหน, ภาควิชาอะไร
   - ตัวอย่าง: "อาจารย์ที่เชี่ยวชาญด้าน AI คือใคร?"
   - ตัวอย่าง: "อาจารย์...ถนัดด้านไหน"
   - ตัวอย่าง: "อาจารย์ที่เป็นที่ปรึกษาโปรเจคเกี่ยวกับaiมีใครบ้าง"

6. project_advice: ขอคำแนะนำหรือไอเดียโปรเจค
   - ตัวอย่าง: "ช่วยแนะนำไอเดียโปรเจคด้าน Machine Learning"
   - ตัวอย่าง: "อยากทำโปรเจค IoT มีไอเดียอะไรบ้าง?"

7. advisor_projects: ถามว่า 'อาจารย์ XXX เป็นที่ปรึกษาโปรเจคไหนบ้าง'
   - ตัวอย่าง: "อาจารย์สมชายเป็นที่ปรึกษาโปรเจคอะไรบ้าง?"
   - ตัวอย่าง: "โปรเจคของ อ.ดร.วิชัย มีอะไรบ้าง?"

8. chitchat: คำถามหรือคำทักทายทั่วไป
   - ตัวอย่าง: "สวัสดี"
   - ตัวอย่าง: "คุณเป็นใคร?"

9. unknown: ไม่สามารถจัดเข้าหมวดใดได้
   - ตัวอย่าง: "!!!@@@@"

ข้อความจากผู้ใช้:
"{question}"

กรุณาตอบเพียงชื่อ intent เท่านั้น
"""
    try:  
        response = ask_typhoon(system_prompt=prompt) 
        print("response from typhoon",response) 
        intent = response.strip().lower()  # ตัดช่องว่างและเปลี่ยนเป็นตัวพิมพ์เล็ก
        allowed_intents = {  
            "ranking", "download", "metadata_query",
            "answer_based_on_documents", "lecturer_info",
            "project_advice", "advisor_projects",
            "chitchat", "unknown"
        }
        return intent if intent in allowed_intents else "unknown"  # ถ้า intent ไม่ตรงกับชุดที่อนุญาต ให้คืนค่า "unknown"
   
    except Exception as e:  
        print(f"[Intent classification error]: {e}")  
        return "unknown"  
